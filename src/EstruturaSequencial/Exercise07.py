# Faça um Programa que calcule a área de um quadrado,
# em seguida mostre o dobro desta área para o usuário.

lado = float(input('Digite o tamanho do lado do quadrado em centimetros: '))
area = lado**2
print('A área do quadrado é ', area, 'centimetros.')
area = area*2
print('O dobro da área é ', area, 'centimetros.')
